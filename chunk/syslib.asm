NMI	jmp	>0
RESET	jmp	>Start

IRQ_CN	FCB	0	Handler count
IRQ_HN	FDB	0,0,0,0,0,0,0,0	Enough for 8 handlers

FIRQ_CN	FCB	0	Handler count
FIRQ_HN	FDB	0,0,0,0,0,0,0,0	Enough for 8 handlers

	ENDSECTION

