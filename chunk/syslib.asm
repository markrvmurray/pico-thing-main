	SECTION	TEXT

INITIRQ	PSHS	A,U
	LDA	IRQ_CN
	LDU	#IRQ_HN
	STX	A,U
	INCA
	INCA
	STA	IRQ_CN
	PULS	A,U,PC

INITFIRQ	PSHS	A,U
	LDA	FIRQ_CN
	LDU	#FIRQ_HN
	STX	A,U
	INCA
	INCA
	STA	FIRQ_CN
	PULS	A,U,PC

IRQISR	LDA	IRQ_CN
	LDU	#IRQ_HN
I@0	TSTA
	BEQ	I@99
	DECA
	DECA
	PSHS	A		; protect counter: handler may clobber A
	JSR	[A,U]
	PULS	A		; restore counter
	BRA	I@0
I@99	RTI

FIRQISR	PSHS	A,U
	LDA	FIRQ_CN
	LDU	#FIRQ_HN
I@0	TSTA
	BEQ	I@99
	DECA
	DECA
	PSHS	A		; protect counter: handler may clobber A
	JSR	[A,U]
	PULS	A		; restore counter
	BRA	I@0
I@99	PULS	A,U
	RTI

	ENDSECTION

	SECTION	SYSVECTORS

